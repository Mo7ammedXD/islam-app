// دالة للحصول على الأحاديث حسب المجموعة
export function getHadithsByCollection(collectionId: string, page: number = 1, limit: number = 20): IHadithList {
  let items: IHadithItems[] = [];
  let name = '';
  
  switch (collectionId) {
    case 'abu-dawud':
      items = abuDawudHadiths;
      name = 'سنن أبي داود';
      break;
    case 'bukhari':
      items = bukhariHadiths;
      name = 'صحيح البخاري';
      break;
    case 'muslim':
      items = muslimHadiths;
      name = 'صحيح مسلم';
      break;
    default:
      items = abuDawudHadiths;
      name = 'سنن أبي داود';
  }

  // تطبيق الصفحات
  const startIndex = (page - 1) * limit;
  const paginatedItems = items.slice(startIndex, startIndex + limit);

  return {
    name,
    items: paginatedItems,
    pagination: {
      totalItems: items.length,
      limit,
      currentPage: page
    }
  };
}

// دالة للبحث عن حديث معين برقمه
export function getHadithByNumber(collectionId: string, number: number): IHadithItems | null {
  let items: IHadithItems[] = [];
  
  switch (collectionId) {
    case 'abu-dawud':
      items = abuDawudHadiths;
      break;
    case 'bukhari':
      items = bukhariHadiths;
      break;
    case 'muslim':
      items = muslimHadiths;
      break;
    default:
      items = abuDawudHadiths;
  }

  return items.find(item => item.number === number) || null;
}